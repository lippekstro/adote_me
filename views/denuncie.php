    <h2>Saiba como denunciar maus-tratos ou crueldade contra animais</h2>
    <p>
      Quando o assunto é denúncia de maus-tratos ou crueldade contra animais,
      o Brasil possui legislação pertinente e autoridades competentes que são
      responsáveis pela manutenção da lei e punição de crimes Caso você
      presencie maus-tratos a animais de quaisquer espécies, sejam domésticos,
      domesticados, silvestres ou exóticos – como abandono, envenenamento,
      presos constantemente em correntes ou cordas muito curtas, manutenção em
      lugar anti-higiênico, mutilação, presos em espaço incompatível ao porte
      do animal ou em local sem iluminação e ventilação, utilização em shows
      que possam lhes causar lesão, pânico ou estresse, agressão física,
      exposição a esforço excessivo e animais debilitados (tração), rinhas,
      etc. –, vá à delegacia de polícia mais próxima para lavrar o Boletim de
      Ocorrência (BO), ou compareça à Promotoria de Justiça do Meio Ambiente.
    </p>

    <p>
      A denúncia de maus-tratos é legitimada pelo Art. 32, da Lei Federal nº.
      9.605, de 12.02.1998 (Lei de Crimes Ambientais) e pela Constituição
      Federal Brasileira, de 05 de outubro de 1988. É possível denunciar
      também ao órgão público competente de seu município, para o setor que
      responde aos trabalhos de vigilância sanitária, zoonoses ou meio
      ambiente. Lembrando que cada município tem legislação diferente,
      portanto caso esta não contemple o tema maus tratos pode utilizar a Lei
      Estadual ou ainda recorrer a Lei Federal.
    </p>

    <h4>Lei de Crimes Ambientais</h4>

    <p>
      Art. 32. Praticar ato de abuso, maus-tratos, ferir ou mutilar animais
      silvestres, domésticos ou domesticados, nativos ou exóticos: Pena -
      detenção, de três meses a um ano, e multa. § 1º. Incorre nas mesmas
      penas quem realiza experiência dolorosa ou cruel em animal vivo, ainda
      que para fins didáticos ou científicos, quando existirem recursos
      alternativos. § 2º. “A pena é aumentada de um sexto a um terço, se
      ocorre morte do animal.”
    </p>

    <h4>Constituição Federal Brasileira</h4>
    <p>
      rt. 23. É competência comum da União, dos Estados, do Distrito Federal e
      dos Municípios: VI – proteger o meio ambiente e combater a poluição em
      qualquer de suas formas; VII – preservar as florestas, a fauna e a
      flora; Art. 225. Todos têm o direito ao meio ambiente ecologicamente
      equilibrado, bem de uso comum do povo e essencial à sadia qualidade de
      vida, impondo-se ao poder público e à coletividade o dever de defendê-lo
      e preservá-lo para os presentes e futuras gerações.
    </p>

    <h4>Como proceder nas delegacias</h4>
    <p>
      Cumpre à autoridade policial receber a denúncia e fazer o boletim de
      ocorrência. O policial que se negar a agir estará cometendo crime de
      prevaricação (retardar ou deixar de praticar, indevidamente, ato de
      ofício, ou praticá-lo contra disposição expressa de lei, para satisfazer
      interesse ou sentimento pessoal - art. 319 do Código Penal). Caso isso
      aconteça, há como queixar-se ao Ministério Público ou à Corregedoria da
      Polícia Civil. Assim que o escrivão ouvir seu relato sobre o crime, a
      ele cumpre instaurar inquérito policial ou lavrar Termo Circunstanciado
      de Ocorrência (TCO). Negando-se a fazê-lo, sob qualquer pretexto,
      lembre-o de que ele pode ser responsabilizado por crime de prevaricação,
      previsto no Art. 329 do Código Penal Brasileiro (retardar ou deixar de
      praticar indevidamente, ato de ofício, ou praticá-lo contra disposição
      expressa de lei para satisfazer interesse ou sentimento pessoal). (Leve
      esse artigo por escrito.) Tente descrever com exatidão os fatos
      ocorridos, o local e, se possível, o nome e endereço do(s)
      responsável(s). Também procure levar, caso haja possibilidade, alguma
      evidência, como fotos, vídeos, notícias de jornais, mapas, laudo ou
      atestado veterinário, nome de testemunhas e endereço das mesmas. Quanto
      mais detalhada a denúncia, melhor.
    </p>

    <p>
      Dica: ao ir à delegacia, procure levar por escrito o art.32 da Lei de
      Crimes Ambientais (Lei Federal n.º 9.605 de 1998) que esta descrito
      acima, uma vez que, infelizmente, há policiais que não estão cientes do
      conteúdo dessa lei. Saiba que você não será o autor do Processo Judicial
      que for aberto a pedido do delegado. O Decreto 24645/1934 reza em seu
      artigo 1º - “Todos os animais existentes no país são tutelados do
      estado”, Logo, uma vez concluído o inquérito para apuração do crime, ou
      elaborado TCO, o Delegado o encaminhará ao juízo para abertura da
      competente ação penal onde o Autor da ação será o Estado.
    </p>

    <h4>IBAMA</h4>

    <p>
      As denúncias podem ser feitas pelo telefone 0800 61 8080 (gratuitamente)
      ou pelo email para linhaverde.sede@ibama.gov.br. O IBAMA (Instituto
      Brasileiro do Meio Ambiente e dos Recursos Naturais Renováveis) as
      encaminhará para a delegacia mais próxima do local da agressão.
    </p>

    <h4>Telefones e endereços para denúncia</h4>

    <p>
      Note que o autor do processo judicial será o estado e não você. Sendo
      assim, não tema denunciar. As organizações não governamentais possuem um
      papel importante e insubstituível na sociedade. Porém, exerça a sua
      cidadania. Não se cale frente aos crimes contra os animais e o meio
      ambiente, e exija das autoridades responsáveis às providências previstas
      por lei.
    </p>

